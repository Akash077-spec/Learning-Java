package Basics.string;

public class charAt {
    public static void main(String [] args){
        String program = " Java programming";
        System.out.println(program.charAt(2));
        System.out.println(" first character : " + program.charAt(0));
         System.out.println(" middle  character : " + program.charAt(8));
          System.out.println(" last character : " + program.charAt(program.length()-1));

    }
    
}
